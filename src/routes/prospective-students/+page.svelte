		<p class="eyebrow">For Prospective Students</p>
		<h2>所属希望の方へ</h2>
		<p class="lead">
			If you are interested in joining AID Lab, please first take a little time to understand
			what kind of research we do, what kind of attitude is helpful, and how we usually work.
		</p>
		<p class="lead lead-jp">
			AID Labへの所属を希望する方は，まず研究室の方向性，研究に必要な姿勢，研究室での進め方を知っておいてください。
		</p>
	</div>

	<div class="guidance-list">
		<article>
			<h3>Research Direction</h3>
			<p>
				AID Lab mainly works on VR/AR hardware, optics, and human visual characteristics.
				In the Media Course, there are also other VR/AR laboratories, such as the Takashima
				Lab and the Asano Lab. Please learn about the main direction of each laboratory in
				advance and choose according to your own interests.
			</p>
			<p lang="ja">
				AID Labでは，VR/ARハードウェア，光学，人間の視覚特性を中心とした研究を行っています。
				メディアコースには，高嶋研究室や浅野研究室など，VR/ARに関連する研究室もあります。
				事前に各研究室の主な方向性を調べ，自分の興味に合わせて選んでください。
			</p>
		</article>

		<article>
			<h3>What You Need To Learn</h3>
			<p>
				AID Lab does not require you to already know a lot about optics. However, I hope you
				do not dislike learning new things. In research, we spend a large amount of time
				learning by ourselves, and I am still learning new things too. It is also better if
				you do not dislike mathematics and English. You should know at least one programming
				language. A laboratory is a place for studying new things. To share what we discover,
				we need to record engineering problems in languages that other people can understand:
				common mathematical formulas, common programming logic, and a common natural language,
				which is English.
			</p>
			<p lang="ja">
				AID Labでは，最初から多くの光学知識を持っている必要はありません。ただし，新しいことを学ぶことに抵抗がない
				ことは大切です。研究では，多くの時間を自分で新しい知識を学ぶことに使いますし，胡自身も今でも学び続けています。
				数学や英語にも抵抗がないとなお良いです。少なくとも一つのプログラミング言語は使えるようにしておいてください。
				研究室は新しいものを研究する場所です。私たちが見つけた新しいことを共有するためには，工学的な問題を，
				誰にでも伝わる形で記録する必要があります。例えば，共通の数学式，共通のプログラミングロジック，
				そして共通の自然言語である英語です。
			</p>
		</article>

		<article>
			<h3>Choosing A Research Topic</h3>
			<p>
				If there is something you already want to work on, please feel free to propose it.
				Then we can think together about how to connect it with Hu's research background.
				Because Hu has attended international conferences for many years, she knows a little
				about many topics in XR and HCI. Still, it is best to choose a topic that Hu can
				properly supervise. If you really do not know what to do, Hu also has some topics she
				wants to try, and you can see whether any of them interest you. My advice is that you
				should choose based on your own interest. Interest is the strongest motivation.
			</p>
			<p lang="ja">
				自分でやってみたいテーマがある場合は，ぜひ提案してください。その上で，胡の専門背景とどのように結びつけられるかを
				一緒に考えましょう。胡は長年国際会議に参加してきたため，XR/HCI分野の研究については広く少しずつ知っています。
				ただし，できれば胡がきちんと指導できる範囲で研究を進めるのが望ましいです。何をしたいか分からない場合は，
				胡自身にもいくつかやってみたいテーマがありますので，その中に興味のあるものがあるか見てください。
				私のおすすめは，やはり自分の興味を一番大切にすることです。興味こそが一番大きな原動力です。
			</p>
		</article>

		<article>
			<h3>Publications</h3>
			<p>
				In the short term, AID Lab may not submit much to domestic Japanese conferences. To be
				honest, Hu's Japanese level is not yet high enough to guide students in writing
				Japanese papers. However, Hu has a lot of experience writing English papers, including
				for top venues in this field such as TVCG, IEEE VR, and ISMAR. AID Lab encourages
				students, even undergraduate students, to try publishing papers in English. I will
				write your first paper together with you, so you do not need to worry too much about
				the writing.
			</p>
			<p lang="ja">
				短期的には，AID Labでは日本国内の学会にはあまり投稿しないかもしれません。正直に言うと，胡の日本語力は，
				学生の日本語論文を十分に指導できるレベルにはまだ達していません。一方で，胡には英語論文を書いてきた経験が多くあり，
				本分野のトップジャーナルであるTVCGや，トップ会議であるIEEE VR，ISMARでの発表経験があります。
				AID Labでは，本科生であっても英語論文の投稿に挑戦することを歓迎します。最初の論文は私も一緒に書きますので，
				英語で書くことを過度に心配しなくて大丈夫です。
			</p>
		</article>

		<article>
			<h3>Connections</h3>
			<p>
				Hu also has many domestic and international connections, including the University of
				Tokyo, Institute of Science Tokyo, NAIST, Tohoku University, the University of
				Tsukuba, the University of Stuttgart in Germany, TU Graz in Austria, several
				universities in Canada, and Shanghai Jiao Tong University in China. We encourage
				students to step out into the world.
			</p>
			<p lang="ja">
				胡には国内外に多くのつながりがあります。国内では東京大学，東京科学大学，NAIST，東北大学，筑波大学など，
				海外ではドイツのシュトゥットガルト大学，オーストリアのTU Graz，カナダの複数の大学，
				中国の上海交通大学などとのつながりがあります。AID Labでは，学生が世界へ出ていくことを応援します。
			</p>
		</article>

		<article>
			<h3>Using AI</h3>
			<p>
				AID Lab does not prohibit students from using AI. On the contrary, Hu thinks that
				using AI is part of the future. The difference in how you use AI may either make you
				much stronger, or make you stop thinking and eventually be replaced by AI. In any
				case, you must think by yourself first. AI is a tool for improving your efficiency.
				You are the commander of AI, not someone driven by AI.
			</p>
			<p>
				Thinking by yourself means that you must read papers by yourself. Papers are written
				in English, and it is fine to use AI for translation if you do not understand them.
				If possible, after AI translates one sentence, look back at the original sentence and
				see where exactly you did not understand. It is also fine to ask AI about mathematics,
				but in the end you must understand it yourself, for example why a mathematical model
				is used here and what a function actually means. Please also remember that AI often
				makes mistakes. Sometimes using Google directly is faster than asking AI.
			</p>
			<p lang="ja">
				AID Labでは，学生がAIを使うことを禁止していません。むしろ，胡はAIを使うことがこれからの流れだと考えています。
				ただし，AIの使い方によって，あなたの力が大きく伸びることもあれば，自分で考えなくなりAIに置き換えられてしまうこともあります。
				いずれにしても，まずは必ず自分で考えてください。AIは効率を上げるための道具です。あなたがAIの指揮官であり，
				AIに動かされる側ではありません。
			</p>
			<p lang="ja">
				ここで言う「自分で考える」とは，必ず自分で論文を読むということです。論文は英語で書かれているので，
				分からなければAIで翻訳しても構いません。できれば，AIが一文を翻訳したあとに原文をもう一度見て，
				自分がどこを理解できなかったのかを確認してください。数学が分からない場合にAIに聞くのも構いませんが，
				最終的には，なぜここでこの数理モデルを使うのか，この関数が何を意味しているのかを自分で理解してください。
				ただし，AIはよく間違えます。場合によっては，AIに聞くより直接Googleで調べた方が早いこともあります。
			</p>
		</article>

		<article>
			<h3>Vibe Coding</h3>
			<p>
				Vibe coding is not prohibited. However, the condition is that you understand
				programming logic. Instead of telling AI, "make me this software," it is better to
				explain your requirements clearly: you need a loop, you need an input, what process
				should happen, and what output you want in the end. This requires a certain level of
				programming thinking. You also need to verify AI's results and debug them yourself.
				Otherwise, AI may use up your quota and still not find where the problem is.
			</p>
			<p lang="ja">
				Vibe codingも禁止していません。ただし，前提として，プログラミングのロジックを理解している必要があります。
				AIに「このようなソフトウェアを作って」と頼むよりも，自分の要求を明確に説明する方が大切です。
				例えば，ループが必要なのか，入力が何か，どのような処理を経て，最後にどのような出力が必要なのかを説明する必要があります。
				そのためには一定のプログラミング的思考が必要です。また，AIの結果を検証し，デバッグできなければなりません。
				そうでなければ，AIが利用上限を使い切っても，どこに問題があるのか分からないままになるかもしれません。
			</p>
		</article>

		<article class="warning">
			<h3>Research Proposals Must Be Your Own</h3>
			<p>
				Directly generating an RP with AI is strictly prohibited. If you feed papers into AI
				and ask it to directly generate an RP, most researchers can tell immediately. If Hu
				finds this, the RP will be rejected directly. Your RP idea does not need to be
				extremely strong or novel. The purpose of asking you to write an RP is to see your
				own thinking, not to demand that your RP immediately become a top-conference paper.
				It is completely normal for third-year undergraduate students to have immature ideas.
				That is okay.
			</p>
			<p>
				However, when writing an RP, you must read papers by yourself. If possible, please
				read papers in English, because the most advanced technologies are often written in
				English, the language used to tell the world about good work. Research is usually
				built on the work of people before us. It is not about creating something completely
				new from nothing, and it is not based only on your own imagination. One person's
				knowledge is limited. Something you think is new may have been done long ago. Hu
				still keeps the habit of reading new papers.
			</p>
			<p lang="ja">
				AIでRPを直接生成することは明確に禁止します。論文をAIに入れて，そのままRPを生成させたものは，
				多くの研究者にはすぐに分かります。胡がそれを見つけた場合，そのRPはその場で拒否します。RPのアイデアは，
				非常にすごいものや新規性の高いものである必要はありません。RPを書いてもらう目的は，あなた自身の思考を見るためであり，
				そのRPがそのままトップ会議の研究になることを求めているわけではありません。学部3年生の考えがまだ未熟なのは普通ですし，
				それで問題ありません。
			</p>
			<p lang="ja">
				ただし，RPを書くときは必ず自分で論文を読んでください。可能であれば英語の論文を読んでください。
				最先端の技術は多くの場合英語で書かれており，良い研究を世界に伝えるための言語でもあります。研究は多くの場合，
				過去の人たちの仕事の上に成り立っています。完全に新しいものを何もないところから作るわけでも，
				自分の空想だけで進めるわけでもありません。一人が知っていることは非常に限られています。
				自分では新しいと思ったことでも，ずっと前に誰かが行っているかもしれません。胡自身も今でも新しい論文を読む習慣を続けています。
			</p>
		</article>

		<article>
			<h3>Language, Time, And Equipment</h3>
			<p>
				Hu's Japanese level is enough for daily communication, and she is still slowly
				learning. In the laboratory, Japanese and English are often used together. The lab
				does not have core time. You may come and leave when you want. However, because the
				equipment is in the laboratory, it is still better to work in the lab as much as
				possible. Basically, each student will have their own computer and their own
				equipment.
			</p>
			<p lang="ja">
				胡の日本語力は日常的なコミュニケーションには十分であり，今も少しずつ勉強しています。研究室では，
				日本語と英語を一緒に使うことがよくあります。また，研究室にはコアタイムはありません。
				いつ来て，いつ帰っても構いません。ただし，機材などは研究室にあるため，できるだけ研究室に来て作業することをおすすめします。
				基本的には，一人ひとりに自分のPCと自分の機材を用意する予定です。
			</p>
		</article>

