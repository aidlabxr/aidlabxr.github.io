		<article>
			<h3>Research Direction</h3>
			<p class="heading-jp">研究の方向性</p>
			<p>
				AID Lab mainly works on VR/AR hardware, optics, and human visual characteristics.
				In the Media Course, there are also other VR/AR laboratories, such as the Takashima
				Lab and the Asano Lab. Please learn about the main direction of each laboratory in
				advance and choose according to your own interests.
			</p>
			<p lang="ja">
				AID Labでは，VR/ARハードウェア，光学，人間の視覚特性を中心とした研究を行っています。
				メディアコースには，高嶋研や浅野研など，VR/ARに関連する研究室もあります。
				事前に各研究室の主な方向性を調べ，自分の興味に合わせて選んでください。
			</p>
		</article>

		<article>
			<h3>What You Need To Learn</h3>
			<p class="heading-jp">学んでほしいこと</p>
			<p>
				AID Lab does not require you to already know a lot about optics. However, I hope you
				do not dislike learning new things. In research, we spend a large amount of time
				learning by ourselves, and I am still learning new things too. It also helps if you 
				are not afraid of mathematics and English. You should know at least one programming
				language. Because we are trying to study things that are still new, we also need to
				describe engineering problems in forms that other people can understand and build on:
				common mathematical formulas, common programming logic, and a common natural language,
				which is English.
			</p>
			<p lang="ja">
				AID Labでは，最初から多くの光学知識を持っている必要はありません。ただし，新しいことを学ぶことに抵抗がない
				ことは大切です。研究では，多くの時間を自分で新しい知識を学ぶことに使いますし，胡自身も今でも学び続けています。
				数学や英語にも抵抗がないとなお良いです。少なくとも一つのプログラミング言語は使えるようにしておいてください。
				研究室は新しい知識や技術を生み出す場所ですので，私たちが見つけた新しいことを共有するためには，工学的な問題を，
				誰にでも伝わる形で記録する必要があります。例えば，共通の数学式，共通のプログラミングの考え方，
				そして国際的に広く使われている英語です。
			</p>
		</article>

		<article>
			<h3>Choosing A Research Topic</h3>
			<p class="heading-jp">研究テーマの選び方</p>
			<p>
				If there is something you already want to work on, please feel free to propose it.
				Then we can think together about how to connect it with my research background.
				Because I have attended international conferences for many years, I have at least
				some familiarity with a broad range of topics in XR and HCI. Still, it is best to
				choose a topic that I can properly supervise. If you really do not know what to do,
				I also have some topics I want to try, and you can see whether any of them interest
				you. My advice is that you
				should choose based on your own interest. Interest is the strongest motivation.
			</p>
			<p lang="ja">
				自分でやってみたいテーマがある場合は，ぜひ提案してください。その上で，胡の専門背景とどのように結びつけられるかを
				一緒に考えましょう。胡は長年国際会議に参加してきたため，XR/HCI分野の幅広いトピックについてある程度は把握しています。
				ただし，できればきちんと指導できる範囲で研究を進めるのが望ましいです。何をしたいか分からない場合は，
				こちらにもいくつかやってみたいテーマがありますので，その中に興味のあるものがあるか見てください。
				おすすめは，やはり自分の興味を一番大切にすることです。興味こそが一番大きな原動力です。
			</p>
		</article>

		<article>
			<h3>Publications</h3>
			<p class="heading-jp">論文発表について</p>
			<p>
				In the short term, AID Lab may not submit much to domestic Japanese conferences. To be
				honest, my Japanese is not yet strong enough for me to confidently supervise students writing papers 
				in Japanese. However, I have a lot of experience writing English papers, including
				for top venues in this field such as TVCG, IEEE VR, and ISMAR. AID Lab encourages
				students, even undergraduate students, to try publishing papers in English. I will
				help you with your first paper, so you do not need to worry too much about
				the writing.
			</p>
			<p lang="ja">
				短期的には，AID Labでは日本国内の学会にはあまり投稿しないかもしれません。正直に言うと，胡の日本語力は，
				学生の日本語論文を十分に指導できるレベルにはまだ達していません。一方で，胡には英語論文を書いてきた経験が多くあり，
				本分野のトップジャーナルであるTVCGや，トップカンファレンスであるIEEE VR，ISMARでの発表経験があります。
				AID Labでは，学部生であっても英語論文の投稿に挑戦することを歓迎します。最初の論文は一緒に書きますので，
				英語で書くことを過度に心配しなくて大丈夫です。
			</p>
		</article>

